package DistributivoVJohnny.modelo;

/**
 *
 * @author Usuario
 */
public class MateriasCarrera {
    
    private int id;
    private String nombre; 
    private int horas; 
    private int ciclo; 
    
    public MateriasCarrera(){ 
        
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getHoras() {
        return horas;
    }

    public void setHoras(int horas) {
        this.horas = horas;
    }

    public int getCiclo() {
        return ciclo;
    }

    public void setCiclo(int ciclo) {
        this.ciclo = ciclo;
    }
    
    
}


    public void entrarApp() {
        String cedula = txtUsuario.getText(); 
        boolean esAdmin = false; 
        for (int i = 0; i < 3; i++) {
            if(adminCedula[i].equals(cedula)){
                esAdmin = true;
                break; 
            }
        }
        
        if (esAdmin && String.valueOf(txtPassword.getPassword()).equals("soyadmin")) {
            AdministracionVtn admin = new AdministracionVtn();
            admin.setVisible(true);
            this.dispose();
        }

        if (autenticar()) {
            App app = new App();
            if (userDocente != null) {
                app.setUserDocente(userDocente);
            }

            if (userEstudiante != null) {
                app.setUserEstudiante(userEstudiante);
            }
            app.bienvenida(usuario);
            app.setVisible(true);
            //Borramos los datos de aqui 
            userDocente = null;
            userEstudiante = null;
            this.dispose();
        }
    }


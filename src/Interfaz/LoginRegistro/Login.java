    public boolean autenticar() {
        boolean encontrado = false;
        String cedula = txtUsuario.getText();
        String contrasena = String.valueOf(txtPassword.getPassword());
        usuario = Conexion_Consultas.ingreso(cedula, contrasena);

        System.out.println(usuario);
        System.out.println(userDocente);
        System.out.println(userEstudiante);

        if (usuario.equals("")) {
            txtUsuarioNoReg.setVisible(true);
            encontrado = false;
        }
        if (userDocente != null) {
            if (userDocente.getContrasenaUsuario().equals(contrasena)) {
                System.out.println("Bienvenido " + usuario);
                encontrado = true;
            } else {
                lblError.setVisible(true);
            }
        } else if (userEstudiante != null) {
            if (userEstudiante.getContrasenaUsuario().equals(contrasena)) {
                System.out.println("Bienvenido " + usuario);
                encontrado = true;
            } else {
                lblError.setVisible(true);
            }
        } else {
            System.out.println("No entramos en ninguno de los tipo de usuario");
        }
        return encontrado;
    }


    private void actualizarTblUsuarios(String modo){
        ArrayList<ArrayList<String>> valores = SQLUsuario.consultarUsuarios(modo); 
        
        modeloTblUser.setRowCount(0); 
        
        for (int i = 0; i < valores.size(); i++) {
          
               Object val [] = new Object [valores.get(i).size()]; 
               val = valores.get(i).toArray(); 
               
               modeloTblUser.addRow(val);        
        }   
    }


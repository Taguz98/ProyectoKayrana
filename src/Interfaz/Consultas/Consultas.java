        Validaciones vali = new Validaciones(); 
        
        String cedula = txtBuscar.getText(); 
        
        if (!vali.esCedula(cedula)) {
             buscar = false; 
        }
        
        if (buscar) {
            actualizarTblUsuarios(cedula);
        }
        
        

    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        boolean guardar = true;

        int id_actividad = 0;
        if (actividades.size() > 0) {
            id_actividad = actividades.get(actividades.size() - 1).getId_actividad();
        }

        id_actividad++;

        String actividad = txtActividades.getText();
        if (!valido.esLetras(actividad)) {
            lblErrorMateria.setVisible(true);
            guardar = false;
        } else if (!nuevaActividad(actividad)) {
            lblErrorNuevaMateria.setVisible(true);
            guardar = false;
        }

        if (guardar) {

            if (!editando) {
                Actividad act = new Actividad();
                act.setId_actividad(id_actividad);
                act.setNombre_actividad(actividad);
                act.setActividad_elim(false);

                SQLActividades.insertarActividad(act);
            }

            if (editando) {
                modeloTabla.setValueAt(actividad, filaEditar, 1);

                Actividad act = new Actividad();
                act.setId_actividad(Integer.parseInt(tblActividades.getValueAt(posFila, 0).toString()));
                act.setNombre_actividad(actividad);
                act.setActividad_elim(false);

                SQLActividades.editarActividad(act);

                editando = false;
            }

            txtActividades.setText("");

            btnAvilitados = Similitudes.btnsModoInactivo(btnEditar, btnEliminar);

            //Actualizamos nuestra tabla 
            actualizarTabla();

            //Ocultanos nuevamente los errores
            lblErrorMateria.setVisible(false);
            lblErrorNuevaMateria.setVisible(false);
        }

    }//GEN-LAST:event_btnGuardarMouseClicked


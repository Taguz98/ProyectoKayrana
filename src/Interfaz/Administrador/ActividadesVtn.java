    private void actualizarTabla() {
        //False son todas las actividades sin ningun filtro 
        actividades = SQLActividades.cargarActividades(false);
        //True solo trae las actividades que no esten eliminadas 
        actividadesF = SQLActividades.cargarActividades(true);

        if (actividadesF.size() > 0) {
            modeloTabla.setRowCount(0);

            for (int i = 0; i < actividadesF.size(); i++) {
                Object valores[] = {actividadesF.get(i).getId_actividad(),
                    actividadesF.get(i).getNombre_actividad()};
                modeloTabla.addRow(valores);
            }
        }
    }

    public boolean nuevaActividad(String materia) {
        boolean datosNuevos = true;
        String materiaComparar;

        try {
            for (int i = 0; i < actividades.size(); i++) {
                materiaComparar = actividades.get(i).getNombre_actividad();

                if (materia.equalsIgnoreCase(materiaComparar)) {
                    SQLActividades.eliminarActividad(actividades.get(i).getId_actividad(), false);
                    datosNuevos = false;
                    actualizarTabla(); 
                    break;
                }
            }

        } catch (ArrayIndexOutOfBoundsException e) {
            System.out.println("Error: " + e.getMessage());
        }

        return datosNuevos;
    }


            if (posFila >= 0 && btnAvilitados) {
                SQLActividades.eliminarActividad(Integer.parseInt(tblActividades.getValueAt(posFila, 0).toString()), true);
                
                modeloTabla.removeRow(posFila); 
            }

            //Cambiamos los botones a inactivos

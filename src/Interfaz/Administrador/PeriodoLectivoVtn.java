    private void actualizarComboCarrera(){
        carrerasDB = SQLCarreras.cargarCarrerasDB(true); 
        comboCarrera.removeAllItems();
        
        for (int i = 0; i < carrerasDB.size(); i++) {
            comboCarrera.addItem(carrerasDB.get(i).getNombre_Carrera());
        }
    }

    private void actualizarTblPeriodoLectivo() {
        modeloTabla.setRowCount(0); 
        
        if (!SQLPeriodoLectivo.cargarPeriodosLectivos(true).isEmpty()) {
            periodos = SQLPeriodoLectivo.cargarPeriodosLectivos(true);

            for (int i = 0; i < periodos.size(); i++) {

                if (periodos.get(i).isPer_lect_activo()) {
                    Object[] valores = {periodos.get(i).getId_per_lect(),
                        periodos.get(i).getFecha_ini_per_lect(),
                        periodos.get(i).getFecha_fin_epr_lect(),
                        periodos.get(i).getAnio_per_lect(),
                        nomCarrera(periodos.get(i).getFk_carrera())};

                    modeloTabla.addRow(valores);
                }
            }
        }
    }
    
    private String nomCarrera(int fk_carrera){
        String nombre = "";
        for (int i = 0; i < carrerasDB.size(); i++) {
            if (carrerasDB.get(i).getId_carrera() == fk_carrera) {
                nombre  = carrerasDB.get(i).getNombre_Carrera();
                break; 
            }
        }
        return nombre; 
    }


    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        boolean guardar = true;

        int id_periodo = 0;

        if (periodos.size() > 0) {
            id_periodo = periodos.get(periodos.size() - 1).getId_per_lect();
        }
        id_periodo++;

        //String fechaIni =  this.fechaInicio.getDateFormatString(); 
        String fechaIni = new SimpleDateFormat("dd/MM/yyyy").format(this.fechaInicio.getDate());

        //String fechaFin = this.fechaFin.getDateFormatString(); 
        String fechaFin = new SimpleDateFormat("dd/MM/yyyy").format(this.fechaFin.getDate());;

        String anio = String.valueOf(this.anio.getValue());
        if (!valido.esAnio(anio)) {
            lblErrorAnio.setVisible(true);
            guardar = false;
        }

        int pos = comboCarrera.getSelectedIndex();
        int fk_carrera = carrerasDB.get(pos).getId_carrera();

        for (int i = 0; i < periodos.size(); i++) {
            if (periodos.get(i).getFecha_ini_per_lect().equals(fechaIni)
                    && periodos.get(i).getFecha_fin_epr_lect().equals(fechaFin)
                    && periodos.get(i).getAnio_per_lect().equals(anio)) {
                guardar = false;
                lblFondo.setVisible(true);
                break;
            }
        }

        System.out.println(anio);

        //Si no encontraron ningun error se guarda
        if (guardar) {

            if (!editando) {

                Periodo_Lectivo perio = new Periodo_Lectivo();
                perio.setId_per_lect(id_periodo);
                perio.setFecha_ini_per_lect(fechaIni);
                perio.setFecha_fin_epr_lect(fechaFin);
                perio.setAnio_per_lect(anio);
                perio.setPer_lect_activo(true);
                perio.setFk_carrera(fk_carrera); 
                

                SQLPeriodoLectivo.insertarPeriodoLectio(perio);

            }

            if (editando) {
                //Seteamos los valores en la posicion que dimos click al editar
                modeloTabla.setValueAt(fechaInicio, filaEditar, 1);
                modeloTabla.setValueAt(fechaFin, filaEditar, 2);
                modeloTabla.setValueAt(anio, filaEditar, 3);

                Periodo_Lectivo perio = new Periodo_Lectivo();
                perio.setId_per_lect(Integer.parseInt(tblPeriodoLectivo.getValueAt(filaEditar, 0).toString()));
                perio.setFecha_ini_per_lect(fechaIni);
                perio.setFecha_fin_epr_lect(fechaFin);
                perio.setAnio_per_lect(anio);
                perio.setFk_carrera(fk_carrera); 

                SQLPeriodoLectivo.editarPeriodoLectivo(perio);

                editando = false;
            }

            //Cambiamos el estado de los botones nuevamente
            btnEliminar.setBackground(new Color(118, 125, 127));

            btnEliminar.setEnabled(false);

            //Ocultamos todos los errores
            lblErrorFechaInicio.setVisible(false);
            lblErrorFechaFin.setVisible(false);
            lblErrorAnio.setVisible(false);
            
            actualizarTblPeriodoLectivo();

        }
    }//GEN-LAST:event_btnGuardarMouseClicked

